int	ft_sqrt(int nb)
{
	long	i;
	long	j;

	j = nb;
	if (j <= 0)
		return (0);
	if (j == 1)
		return (1);
	i = 2;
	if (j >= 2)
	{
		while (i * i <= j)
		{
			if (i * i == j)
				return (i);
			i++;
		}
	}
	return (0);
}
